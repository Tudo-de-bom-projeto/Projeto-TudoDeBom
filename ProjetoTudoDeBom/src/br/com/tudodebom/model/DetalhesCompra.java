package br.com.tudodebom.model;



/*
 * Classe para representar a entidade detalhes_compra
 */
public class DetalhesCompra {

	/* Atributos */
	private Double precoEstendido;
	private Integer qtdCompra;
	private Integer numeroSequencia;
	private Integer idProduto;
	private Integer codigoCompra;
	
	
	public Double getPrecoEstendido() {
		return precoEstendido;
	}
	public void setPrecoEstendido(Double precoEstendido) {
		this.precoEstendido = precoEstendido;
	}
	public Integer getQtdCompra() {
		return qtdCompra;
	}
	public void setQtdCompra(Integer qtdCompra) {
		this.qtdCompra = qtdCompra;
	}
	public Integer getNumeroSequencia() {
		return numeroSequencia;
	}
	public void setNumeroSequencia(Integer numeroSequencia) {
		this.numeroSequencia = numeroSequencia;
	}
	public Integer getIdProduto() {
		return idProduto;
	}
	public void setIdProduto(Integer idProduto) {
		this.idProduto = idProduto;
	}
	public Integer getCodigoCompra() {
		return codigoCompra;
	}
	public void setCodigoCompra(Integer codigoCompra) {
		this.codigoCompra = codigoCompra;
	}
	
	
	
	
	
}
